# identity + filament helpers


def _apply_identity_swap(
    source_cfg: dict[str, Any],
    reference_cfg: dict[str, Any],
    diff: DiffBuilder,
) -> dict[str, Any]:
    merged = dict(source_cfg)
    for key in IDENTITY_COPY_KEYS:
        if key not in reference_cfg:
            continue
        old = merged.get(key)
        new = reference_cfg[key]
        if old != new:
            diff.record_identity_swap(key, old, new)
        merged[key] = new
    return merged


def _collapse_extruder_lists(
    settings: dict[str, Any],
    reference: dict[str, Any],
) -> dict[str, Any]:
    """Collapse per-extruder list values to scalars where reference expects a scalar.

    Bambu stores many print settings as [val_ext0, val_ext1, ...].  For keys
    where the U1 reference profile uses a single scalar, take the first element
    so downstream clamping operates on a comparable type.  filament_* arrays are
    intentionally excluded — they are handled by ``_normalise_filament_arrays``.
    """
    out = dict(settings)
    for key, ref_val in reference.items():
        if key.startswith("filament_") or key not in out:
            continue
        cur_val = out[key]
        if isinstance(cur_val, list) and not isinstance(ref_val, list):
            out[key] = cur_val[0] if cur_val else ref_val
    return out


def _map_filaments_to_reference(
    merged: dict[str, Any],
    source_cfg: dict[str, Any],
    reference_cfg: dict[str, Any],
    n_target: int,
) -> None:
    """Replace source filament profiles with target-printer-certified equivalents.

    Source filament_type is matched against reference filament_type to find
    the closest certified profile (e.g. PLA → Bambu PLA Basic @BBL H2S).
    filament_settings_id, filament_ids, and filament_nozzle_map are updated;
    filament_colour and all embedded temp/speed settings are kept from source.
    """
    src_types = source_cfg.get("filament_type") or []
    src_sids = source_cfg.get("filament_settings_id") or []
    ref_types = reference_cfg.get("filament_type") or []
    ref_sids = reference_cfg.get("filament_settings_id") or []
    ref_fids = reference_cfg.get("filament_ids") or []
    ref_nmap = reference_cfg.get("filament_nozzle_map") or []

    # Build type→ref map, preferring "Basic" profiles as the generic fallback
    # so that unknown/generic source filaments land on the most permissive
    # certified profile rather than the first alphabetical one.
    type_to_ref: dict[str, int] = {}
    for i, (t, sid) in enumerate(zip(ref_types, ref_sids)):
        if t not in type_to_ref or "basic" in sid.lower():
            type_to_ref[t] = i

    # Extract distinguishing keywords from each reference profile name so we
    # can match a source "Bambu PLA Matte" → Matte ref, "Silk" → Silk+ ref.
    _VARIANT_KEYWORDS = ["matte", "silk", "basic", "high flow", " hf"]
    keyword_to_ref: dict[str, int] = {}
    for i, sid in enumerate(ref_sids):
        sid_l = sid.lower()
        for kw in _VARIANT_KEYWORDS:
            if kw in sid_l and kw not in keyword_to_ref:
                keyword_to_ref[kw] = i

    new_sids, new_fids, new_nmap = [], [], []
    for i in range(n_target):
        src_type = src_types[i] if i < len(src_types) else "PLA"
        src_sid = (src_sids[i] if i < len(src_sids) else "").lower()

        # 1. keyword match — preserves Matte→Matte, Silk→Silk+, etc.
        ref_idx = next(
            (keyword_to_ref[kw] for kw in _VARIANT_KEYWORDS if kw in src_sid and kw in keyword_to_ref),
            None,
        )
        # 2. type match (Basic-preferred fallback)
        if ref_idx is None:
            ref_idx = type_to_ref.get(src_type)

        if ref_idx is not None:
            new_sids.append(ref_sids[ref_idx] if ref_idx < len(ref_sids) else f"Generic {src_type}")
            new_fids.append(ref_fids[ref_idx] if ref_idx < len(ref_fids) else "")
            new_nmap.append("1")
        else:
            # No type match — keep as Generic and use Standard nozzle.
            new_sids.append(f"Generic {src_type}" if src_type else "Generic PLA")
            cur_fids = merged.get("filament_ids") or []
            new_fids.append(cur_fids[i] if i < len(cur_fids) else "")
            new_nmap.append("0")

    merged["filament_settings_id"] = new_sids
    merged["filament_ids"] = new_fids
    merged["filament_nozzle_map"] = new_nmap


def _normalise_filament_arrays(
    merged: dict[str, Any],
    source_cfg: dict[str, Any],
    diff: DiffBuilder,
    slot_map: dict[int, int] | None = None,
    n_target: int = _U1_TOOLHEADS_DEFAULT,
) -> dict[int, int]:
    """Reorder, pad, and truncate every ``filament_*`` array to n_target slots.

    ``slot_map`` is 0-indexed source_slot → 0-indexed target_toolhead.
    If empty/None the identity map is used (slot N → toolhead N).
    Source slots that map beyond n_target are dropped; unpopulated target
    slots are filled by repeating the last mapped entry.
    """
    source_ids = source_cfg.get("filament_settings_id") or []
    n_src = len(source_ids)

    # Build the canonical remap: every source slot → target toolhead.
    remap: dict[int, int] = {}
    for i in range(n_src):
        if slot_map and i in slot_map:
            target = slot_map[i]
        else:
            target = i
        if target >= n_target:
            target = -1  # dropped
        remap[i] = target

    for i, target in remap.items():
        diff.extend_slot_remaps(
            [SlotRemapEvent(
                from_index=i,
                to_index=target,
                filament_id=source_ids[i] if i < len(source_ids) else None,
            )]
        )

    # Reorder every filament_* list according to the remap.
    for key, value in list(merged.items()):
        if not (key.startswith("filament_") and isinstance(value, list)):
            continue
        if not value:
            continue

        # Build a target-length list, placing source values at mapped positions.
        # First-write wins: primary slots (lower index) take precedence over
        # overflow colours merged into the same target.
        out: list[Any] = [None] * n_target
        fallback = value[-1] if value else None
        for src_i, tgt_i in remap.items():
            if tgt_i >= 0 and src_i < len(value) and out[tgt_i] is None:
                out[tgt_i] = value[src_i]

        # Fill any unpopulated target slots with the fallback (last source value).
        for j in range(n_target):
            if out[j] is None:
                out[j] = fallback

        merged[key] = out

    return remap


