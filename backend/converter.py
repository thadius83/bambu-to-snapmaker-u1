# Keys that must come from the U1 reference profile — strictly hardware identity.
# Everything else preserves source intent.
IDENTITY_COPY_KEYS: tuple[str, ...] = (
    "printer_settings_id",
    "printer_model",
    "printer_variant",
    "printable_area",
    "printable_height",
    "nozzle_diameter",
    "nozzle_type",
    "nozzle_volume",
    "compatible_printers",
    "compatible_printers_condition",
    "bed_exclude_area",
    # Orca protocol version — prevents "generated by a different application" warning.
    "version",
)

# Bambu uses "0" as an auto-sentinel for these filament-role slot keys; U1 Orca
# requires an explicit 1-based slot number.  Only the sentinel is replaced —
# any intentional non-zero assignment from the source is preserved.
_FILAMENT_SLOT_SENTINEL_KEYS: frozenset[str] = frozenset({
    "wall_filament",
    "sparse_infill_filament",
    "solid_infill_filament",
    "support_filament",
    "support_interface_filament",
    "wipe_tower_filament",
})

# Bambu uses -1 as an "inherit / auto" sentinel for these keys; Orca requires
# an explicit value.  When the source contains -1 we substitute the reference
# profile's value so Orca gets something valid.
_NEGATIVE_SENTINEL_KEYS: frozenset[str] = frozenset({
    "raft_first_layer_expansion",
    "tree_support_wall_count",
    "prime_tower_lift_height",
})

# Direct value remaps for Bambu enum strings that Orca does not recognise.
_VALUE_REMAP: dict[str, dict[str, str]] = {
    "ensure_vertical_shell_thickness": {"disabled": "none", "enabled": "ensure_all", "partial": "ensure_all"},
    "support_style": {"tree_organic": "default"},
}

# Snapmaker U1 has 4 physical toolheads.
_U1_TOOLHEADS_DEFAULT = 4

PROJECT_SETTINGS = "Metadata/project_settings.config"
MODEL_SETTINGS = "Metadata/model_settings.config"
SLICE_INFO = "Metadata/slice_info.config"
CUSTOM_GCODE_XML = "Metadata/custom_gcode_per_layer.xml"
MODEL_3D = "3D/3dmodel.model"

