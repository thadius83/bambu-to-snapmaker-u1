@dataclass
class ConversionResult:
    output_path: Path
    diff: DiffReport


def convert(
    *,
    source_path: Path,
    reference_path: Path,
    output_path: Path,
    settings: ConversionSettings,
    rules: list[RuleDefinition],
    preserve_bambu_metadata: bool = False,
) -> ConversionResult:
    """Run the full pipeline. Returns the output path + DiffReport.

    ``preserve_bambu_metadata`` — when True, Bambu-specific metadata files
    (filament_sequence.json, cut_information.xml, auxiliary.xml) are kept in
    the output and model_settings.config is not rewritten.  Use this for
    Bambu-to-Bambu cross-printer conversion where those files remain valid.
    """
    t0 = time.monotonic()
    src_size = source_path.stat().st_size
    log.info(
        "START  %s  (%.1f KB)  profile=%s  rules=%s  clamp=%s  painting=%s",
        source_path.name,
        src_size / 1024,
        reference_path.stem,
        settings.apply_rules,
        settings.clamp_speeds,
        settings.preserve_color_painting,
    )

    source_cfg, _ = read_source_settings(source_path)
    reference_cfg = read_project_settings(reference_path)

    with zipfile.ZipFile(source_path) as _znames:
        _src_names = _znames.namelist()
    _is_painted = is_painted_model(_src_names, len(source_cfg.get("filament_settings_id") or []))

    src_printer = source_cfg.get("printer_model", "?")
    src_lh = source_cfg.get("layer_height", "?")
    src_filaments = source_cfg.get("filament_settings_id") or []
    log.info(
        "SOURCE printer=%s  layer_height=%s  filaments=%s",
        src_printer,
        src_lh,
        src_filaments,
    )

    diff = DiffBuilder(
        source_filename=source_path.name,
        output_filename=output_path.name,
        reference_profile=reference_path.stem,
    )

    # 3. identity swap
    merged = _apply_identity_swap(source_cfg, reference_cfg, diff)
    # Append suffix so Orca cannot exact-match a U1 library profile and load
    # its defaults over the embedded source settings.
    if merged.get("print_settings_id"):
        merged["print_settings_id"] = merged["print_settings_id"] + " (converted)"
    id_swaps = len(diff.build().identity_swaps)
    log.info("STAGE  identity-swap: %d keys updated", id_swaps)

    # 4. g-code swap
    merged, gcode_events = swap_gcode(merged, reference_cfg)
    diff.extend_gcode_swaps(gcode_events)
    log.info("STAGE  gcode-swap: %d blocks replaced", len(gcode_events))

    # 5. key filter against reference schema
    schema_keys = set(reference_cfg.keys())
    # custom_gcode_per_layer: per-layer colour-swap/pause markers absent from reference but needed by Orca.
    # internal_bridge_support_thickness: present in fdm_process_common at default (0.8) so not written
    # to reference .3mf exports, but is a valid Orca print-intent key that should flow from source.
    merged, dropped = filter_to_schema(
        merged,
        schema_keys,
        keep_keys=["custom_gcode_per_layer", "internal_bridge_support_thickness"],
    )
    diff.extend_keys_dropped(dropped)
    log.info("STAGE  key-filter: %d keys dropped", len(dropped))

    # Re-overlay reference defaults for any key absent from source.
    for k, v in reference_cfg.items():
        merged.setdefault(k, v)

    # Snapmaker Orca has a bug with rib prime towers; force rectangle (normal)
    # tower mode in U1 output regardless of source.  Bambu uses different keys
    # (prime_tower_fillet_wall / prime_tower_rib_wall) that are stripped by the
    # key filter, so without this the output inherits whatever the reference has.
    if not preserve_bambu_metadata:
        merged["wipe_tower_wall_type"] = "rectangle"

    # For Bambu-to-Bambu: overwrite print_compatible_printers from the reference
    # so Bambu Studio recognises the target printer instead of showing the source
    # printer's compatible list (e.g. X1C/P1S list on an H2S file).
    if preserve_bambu_metadata and "print_compatible_printers" in reference_cfg:
        merged["print_compatible_printers"] = reference_cfg["print_compatible_printers"]

    # Clear inherits_group — it names library profiles (print + per-filament)
    # that Orca will load and apply on top of embedded settings, overriding
    # source values like initial_layer_print_height and filament_settings_id.
    if "inherits_group" in merged:
        n = len(merged["inherits_group"]) if isinstance(merged["inherits_group"], list) else 0
        merged["inherits_group"] = [""] * n

    # 5b. collapse per-extruder lists → scalars where reference uses a scalar.
    # Bambu stores many settings as [val_ext1, val_ext2, ...]; U1 Orca expects
    # a single value.  Take the first element so source intent is preserved;
    # the clamp step that follows will cap it to U1 limits.
    merged = _collapse_extruder_lists(merged, reference_cfg)
    log.info("STAGE  collapse-lists: done")

    # 5c. Filament-role slot sentinels: Bambu uses "0" (auto); U1 needs explicit "1".
    n_slot = 0
    for key in _FILAMENT_SLOT_SENTINEL_KEYS:
        if merged.get(key) in ("0", 0):
            merged[key] = "1"
            n_slot += 1

    # 5d-pre. Zero line-width sentinels: Bambu uses 0 to mean "derive from nozzle
    # diameter." When the source nozzle differs from the U1's (e.g. 0.2mm → 0.4mm)
    # these zeros produce 0-width paths in Orca, breaking both printing and
    # paint-driven filament changes. Replace with reference value.
    n_lw = 0
    for key, val in list(merged.items()):
        if not (key == "line_width" or key.endswith("_line_width")):
            continue
        if key == "brim_width":
            continue  # 0 = no brim, intentional
        try:
            if float(str(val)) == 0.0 and key in reference_cfg:
                ref_val = reference_cfg[key]
                if float(str(ref_val)) > 0:
                    merged[key] = ref_val
                    n_lw += 1
        except (TypeError, ValueError):
            pass
    if n_lw:
        log.info("STAGE  sentinels: lw_zero=%d", n_lw)

    # 5d. Negative sentinels: Bambu uses -1 (inherit); substitute reference value.
    n_neg = 0
    for key in _NEGATIVE_SENTINEL_KEYS:
        try:
            if float(str(merged.get(key))) == -1.0 and key in reference_cfg:
                merged[key] = reference_cfg[key]
                n_neg += 1
        except (TypeError, ValueError):
            pass

    # 5e. Enum remaps: Bambu enum strings Orca doesn't recognise.
    n_enum = 0
    for key, mapping in _VALUE_REMAP.items():
        val = merged.get(key)
        if isinstance(val, str) and val in mapping:
            merged[key] = mapping[val]
            n_enum += 1

    if n_slot or n_neg or n_enum:
        log.info("STAGE  sentinels: slot=%d neg=%d enum=%d lw=%d", n_slot, n_neg, n_enum, n_lw)

    # 6. clamp speed / accel ceilings
    if settings.clamp_speeds:
        merged, clamp_events = clamp_numeric_ceilings(merged, reference_cfg)
        diff.extend_values_clamped(clamp_events)
        log.info("STAGE  clamp: %d values clamped", len(clamp_events))
    else:
        log.info("STAGE  clamp: skipped")

    # 6b. disable prime tower when variable layer height is present.
    # Two separate VLH mechanisms both conflict with the prime tower:
    #   layer_config_ranges.xml  — per-layer settings ranges (colour/speed changes)
    #   layer_heights_profile.txt — per-object adaptive height data
    _HAS_LAYER_RANGES = "Metadata/layer_config_ranges.xml"
    _HAS_VLH_PROFILE = "Metadata/layer_heights_profile.txt"
    _has_vlh = _HAS_LAYER_RANGES in _src_names or _HAS_VLH_PROFILE in _src_names
    log.info("STAGE  vlh: %s", _has_vlh)
    if merged.get("enable_prime_tower") in ("1", 1) and _has_vlh:
        merged["enable_prime_tower"] = "0"
        log.info("STAGE  prime-tower: disabled — VLH present (layer_config_ranges or layer_heights_profile)")

    # 6c. organic supports + per-object VLH → Orca cannot slice; fall back to normal(auto)
    # layer_heights_profile.txt carries per-object adaptive height data; tree supports
    # are incompatible with it in Orca.
    _support_type = merged.get("support_type", "")
    if "tree" in str(_support_type) and _HAS_VLH_PROFILE in _src_names:
        merged["support_type"] = "normal(auto)"
        log.info("STAGE  supports: tree→normal(auto) — layer_heights_profile.txt present (VLH conflict)")

    # 7. rules engine
    if settings.apply_rules and rules:
        ctx = FilamentContext.from_settings(source_cfg)
        merged, rule_events = apply_rules(merged, rules, ctx)
        diff.extend_rules_matched(rule_events)
        for ev in rule_events:
            log.info(
                "RULE   %r (priority %d) matched — %d overrides applied",
                ev.rule_name,
                ev.priority,
                len(ev.overrides_applied),
            )
        if not rule_events:
            log.info("STAGE  rules: no rules matched")
    else:
        log.info("STAGE  rules: skipped (apply_rules=%s, n_rules=%d)",
                 settings.apply_rules, len(rules))

    # 8. advanced per-key overrides
    for k, v in settings.advanced_overrides.items():
        merged[k] = v
        diff.record_advanced_override(k, v)
    if settings.advanced_overrides:
        log.info("STAGE  advanced-overrides: %d keys", len(settings.advanced_overrides))

    # 9. filament array normalisation — preserve source slot count exactly.
    # For Bambu-to-Bambu the source count is authoritative (AMS handles any
    # number of slots).  For U1: if the user supplied an explicit slot_map
    # they are committing to a 4-toolhead output; otherwise pass all source
    # slots through so painted models keep every colour for Orca to handle.
    n_src = len(source_cfg.get("filament_settings_id") or [])
    n_ref = len(reference_cfg.get("filament_settings_id") or []) or _U1_TOOLHEADS_DEFAULT
    if preserve_bambu_metadata and n_src > 0:
        n_target = n_src
    elif settings.slot_map:
        n_target = _U1_TOOLHEADS_DEFAULT
    elif _is_painted:
        # No slot_map: skip was chosen — preserve all source colours so Orca
        # can handle slot assignment at re-slice time.
        n_target = n_src
    else:
        _is_non_orca = "printer_model" not in source_cfg
        if n_src > 0:
            # Non-Orca sources (PrusaSlicer, Cura) carry their own filament
            # arrays — pass them all through; don't cap at the reference slot
            # count which is tuned for Bambu prints.
            n_target = n_src if _is_non_orca else min(n_src, n_ref)
        elif "filament_settings_id" in source_cfg:
            # Orca/Bambu source with an explicit empty filament list.
            n_target = n_ref
        else:
            # Non-Orca source with no filament info at all — default to 1 slot.
            n_target = 1
    remap = _normalise_filament_arrays(
        merged, source_cfg, diff, slot_map=settings.slot_map or {}, n_target=n_target
    )
    # Clip or pad per-slot hardware arrays not handled by _normalise_filament_arrays.
    for _hw_key in ("nozzle_diameter", "nozzle_type", "nozzle_volume"):
        _hw_val = merged.get(_hw_key)
        if isinstance(_hw_val, list) and _hw_val:
            if len(_hw_val) > n_target:
                merged[_hw_key] = _hw_val[:n_target]
            elif len(_hw_val) < n_target:
                merged[_hw_key] = _hw_val + [_hw_val[-1]] * (n_target - len(_hw_val))
    log.info("STAGE  filament-normalise: remap=%s", remap)

    # 9b. For Bambu-to-Bambu: remap each source filament slot to the closest
    # target-printer-certified profile from the reference, matched by filament
    # type.  Source filament colours and embedded temps/speeds are preserved;
    # only the profile identity (settings_id, filament_ids, nozzle_map) changes
    # so Bambu Studio can validate the filament against the target nozzle.
    if preserve_bambu_metadata:
        _map_filaments_to_reference(merged, source_cfg, reference_cfg, n_target)
        log.info("STAGE  filament-profile-remap: done")

    # 9c. For Bambu-to-Bambu: rebuild strided filament arrays.
    # Some H2S-specific arrays (e.g. filament_extruder_variant) have 2 entries
    # per slot — one for Standard extruder, one for High Flow.  The source
    # (P1S/X1C) has no such key, so the reference value lands via setdefault.
    # _normalise_filament_arrays then truncates it to n_target entries instead
    # of stride * n_target, breaking HF availability in Bambu Studio.
    # Fix: detect stride from reference slot count and rebuild those arrays.
    if preserve_bambu_metadata:
        n_ref_slots = len(reference_cfg.get("filament_settings_id") or [])
        if n_ref_slots > 0:
            for _key, _ref_val in reference_cfg.items():
                if not _key.startswith("filament_") or not isinstance(_ref_val, list):
                    continue
                if source_cfg.get(_key):  # source had this; normalization was correct
                    continue
                if len(_ref_val) % n_ref_slots != 0:
                    continue
                _stride = len(_ref_val) // n_ref_slots
                if _stride <= 1:
                    continue
                if _key == "filament_self_index":
                    # Self-index is the 1-based slot number for each variant
                    # entry, not a repeatable pattern — must be generated.
                    merged[_key] = [str(s + 1) for s in range(n_target) for _ in range(_stride)]
                else:
                    _slot_pattern = _ref_val[:_stride]
                    merged[_key] = _slot_pattern * n_target
        log.info("STAGE  filament-stride-fix: done (n_ref_slots=%d)", n_ref_slots)

    # 10. optional swap-pause insertion for >4 colour prints
    custom_gcode_xml_override: str | None = None
    if settings.insert_swap_pauses:
        with zipfile.ZipFile(source_path) as _zcheck:
            if CUSTOM_GCODE_XML in _zcheck.namelist():
                raw_xml = _zcheck.read(CUSTOM_GCODE_XML).decode("utf-8")
                colours = merged.get("filament_colour") or []
                pause_cmd = merged.get("machine_pause_gcode", "M600")
                modified_xml, swap_instrs = insert_swap_pauses(
                    raw_xml, remap, colours, pause_cmd
                )
                if swap_instrs:
                    # Swap insertion adds new type=1 pause markers but must not
                    # leave any existing Bambu type=1 commands untouched.
                    custom_gcode_xml_override = rewrite_custom_gcode_per_layer(
                        modified_xml, pause_cmd
                    )
                    diff._report.swap_instructions = swap_instrs
                    log.info("STAGE  swap-pauses: %d pauses inserted", len(swap_instrs))
            else:
                # No per-layer G-code file — this is a painted model. Colour
                # changes are encoded in the model geometry and generated by the
                # slicer at re-slice time; pauses cannot be pre-inserted.
                diff._report.swap_pauses_skipped_painted = True
                log.info("STAGE  swap-pauses: skipped — no custom_gcode_per_layer.xml (painted model)")

    # 11–14. build output archive
    target_printer_model = reference_cfg.get("printer_model", "")
    _write_output_archive(
        source_path=source_path,
        reference_path=reference_path,
        output_path=output_path,
        new_project_settings=merged,
        remap=remap,
        diff=diff,
        settings=settings,
        custom_gcode_xml_override=custom_gcode_xml_override,
        preserve_bambu_metadata=preserve_bambu_metadata,
        target_printer_model=target_printer_model,
    )

    report = diff.build()
    out_size = output_path.stat().st_size
    elapsed = (time.monotonic() - t0) * 1000
    counts = report.counts()
    log.info(
        "DONE   %s  (%.1f KB)  elapsed=%.0f ms  "
        "identity=%d  gcode=%d  dropped=%d  clamped=%d  rules=%d  slots=%d",
        output_path.name,
        out_size / 1024,
        elapsed,
        counts.get("identity_swaps", 0),
        counts.get("gcode_swaps", 0),
        counts.get("keys_dropped", 0),
        counts.get("values_clamped", 0),
        counts.get("rules_matched", 0),
        counts.get("slot_remaps", 0),
    )

    return ConversionResult(output_path=output_path, diff=report)


